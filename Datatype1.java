class Datatype1
{
    public static void main(String Arg[])
    {
        byte a = 100;
        short b = 200;
        int c = 300;
        float d = 400.506f;
        long e = 7000l;
        boolean f = true;
        char ch = 'A';
        double g = 8000.56f;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(ch);
        System.out.println(g);
    }    
}
